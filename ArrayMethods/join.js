// join() method is used to combine the elements of an array into a string

console.log(["slugs","snails","puppy"].join(' and '));     //  "slugs and snails and puppy dog's tails"
console.log(['Giants', 4, 'Rangers', 1].join(' '));        //  "Giants 4 Rangers 1"
console.log([1962,1989,2002,2010].join());                 //  "1962,1989,2002,2010"
console.log([3, ".", 1, 4, 1, 5, 9, 2].join(''));          //   "3.141592"

