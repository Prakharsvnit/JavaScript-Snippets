//Ternary operator
let age = 16
let message

message = (age >= 18) ? "You can drive" : "You can't drive"
console.log(message)

/*
In above example,the age variable is subjected to condition,
so the expression (age >= 18) if evaluates to true,value of 
variable message will be whatever is  written after '?'
and if false value of variable message will be whatever written after ':'.
*/



